    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        try {
            
        try {
            
        Class.forName("com.mysql.jdbc.Driver");

        Connection con;
        
        con = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/home", "root", "");
               
        String stmt = "SELECT Nome FROM aluno where RAaluno = ?";
         
        PreparedStatement consulta;
        
        consulta = (PreparedStatement) con.prepareStatement(stmt);

        consulta.setString(1, jFormattedTextField1.getText());

        ResultSet rsServicos;
        
        rsServicos = consulta.executeQuery();

        while (rsServicos.next()) {   
                    
        String serv = rsServicos.getString(1);
                    
        jTextField1.setText(serv);
                    
        }
        
        rsServicos.close();
        
        con.close();
        
        if (jTextField1.getText().equals("")) {
            
        JOptionPane.showMessageDialog(null, "Esse Numero de inscrição não esta cadastrado");
        
        }
        
        } catch (SQLException e) {
            
        JOptionPane.showMessageDialog(null, "Ocorreu um erro no sistema, se persistir contate o Administrador '" + e + "", "ATENÇÃO",
        JOptionPane.WARNING_MESSAGE);
                
        }
        
        } catch (ClassNotFoundException ex) {
            
        Logger.getLogger(JCadPagamento.class.getName()).log(Level.SEVERE, null, ex);
        
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {

        Class.forName("com.mysql.jdbc.Driver");

        Connection mat;

        mat = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/home", "root", "");

        String query;

        query = "INSERT INTO pagamento(RAaluno,Nome,Modulo,Valor) VALUES (?,?,?,?)";
        
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("")) {

        JOptionPane.showMessageDialog(null, " Todos os campos são obrigatório!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            
        try (PreparedStatement dic = (PreparedStatement) mat.prepareStatement(query)) {

        dic.setString(1, jFormattedTextField1.getText());
        dic.setString(2, jTextField1.getText());
        dic.setString(3, jComboBox1.getSelectedItem().toString());
        dic.setString(4, jTextField2.getText());

        dic.executeUpdate();
        
        mat.close();
        
        JOptionPane.showMessageDialog(null, "Pagamento efetuado com sucesso!");
       
        new JMenu().show();
        
        new JCadPagamento().setVisible(false);
        
        dispose();
        
        }
        
        }

        } catch (SQLException | ClassNotFoundException ex) {
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

