    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
            try {

            Class.forName("com.mysql.jdbc.Driver");

            Connection cons;

            cons = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/home", "root", "");

            String query = "SELECT Nome, Modulo, SUM(Valor) valor FROM pagamento WHERE RAaluno = ? GROUP BY  Modulo";

            PreparedStatement consulta;
        
            consulta = (PreparedStatement) cons.prepareStatement(query);
           
            consulta.setString(1, jFormattedTextField1.getText());

            ResultSet rs;

            rs = consulta.executeQuery();
    
            DefaultTableModel Mode1 = (DefaultTableModel) jTable1.getModel();
            
            jTable1.getColumnModel().getColumn(0).setPreferredWidth(250);
            jTable1.getColumnModel().getColumn(1).setPreferredWidth(30);
            jTable1.getColumnModel().getColumn(2).setPreferredWidth(30);
            
            Mode1.setNumRows(0);

            while (rs.next()) {
                
                Mode1.addRow(new Object[]{rs.getString("Nome"), rs.getString("Modulo"), rs.getString("valor")});
                
            }
            cons.close();
            
            consulta.close();
            
            } catch (ClassNotFoundException | SQLException ex) {
            
            }
            
    }//GEN-LAST:event_jButton1ActionPerformed

